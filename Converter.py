class Converter:
    @staticmethod
    def kg_to_pound(kg):
        return kg * 2.2046

    
    @staticmethod
    def kg_to_oz(kg):
        return kg * 35.274


    @staticmethod
    def oz_to_kg(oz):
        return oz * 0.0283


    @staticmethod
    def pound_to_kg(pound):
        return pound * 0.4535

    @staticmethod
    def oz_to_pound(oz):
        return oz * 0.0625

    

    
    
